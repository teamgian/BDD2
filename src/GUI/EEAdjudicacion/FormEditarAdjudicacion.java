    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            
            MenuPrincipal.Conexion();
            Statement s = MenuPrincipal.connection.createStatement();
            
            
            // MONTO
            String montoAdjudicado = montoText.getText();           
            
            // FECHAS
            String fechainicio = DateFormat.getDateInstance().format(fechaadjudicacion.getDate());
            String fechadetermino = DateFormat.getDateInstance().format(fechatermino.getDate());         
           
            if(fechaadjudicacion.getDate().after(fechatermino.getDate()))
            {
                JOptionPane.showMessageDialog(this, "La fecha de término debe ser posterior a la fecha de adjudicación");
            }
            else
            {
                //edita la adjudicación con los nuevos datos
            int z = s.executeUpdate("UPDATE ADJUDICA SET montoadjudicado="
            +montoAdjudicado+", fechaadjudicacion='"+fechainicio+"', fechadetermino='"+fechadetermino+
                    "' where RUT='"+adjudicacion.getFirstID()+"' AND codigoproyecto ="+adjudicacion.getSecondID());
            
            
            if(z == 1){ JOptionPane.showMessageDialog(this, "Se editó la adjudicación exitosamente");  }
                
            }
            
            
            
            
        } catch(SQLException e){
                 
            System.out.println(e.getSQLState());
                 if(e.getSQLState().equals("23505"))
                 {
                     JOptionPane.showMessageDialog(this, "No se puede agregar la adjudicación debido a que ya existe una con las claves elegidas"
                    );
                 }  
                 else if(e.getSQLState().equals("42703"))
                 {
                     JOptionPane.showMessageDialog(this, "Monto inválido, por favor ingrese un número sin puntos ni otros caracteres");
                 }
                 else if(e.getSQLState().equals("42601"))
                 {
                     JOptionPane.showMessageDialog(this, "LLene campos vacíos");
                 }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

