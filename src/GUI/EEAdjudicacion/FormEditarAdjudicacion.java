    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try{
            MenuPrincipal.Conexion();  
            Statement s = MenuPrincipal.connection.createStatement();
            
            
            String rut = adjudicacion.getFirstID();
            String codigoproyecto = adjudicacion.getSecondID();
            
            
            // obtiene nombre del investigador y titulo del proyecto para mostrarlos en textfields
            ResultSet resultTitulo = s.executeQuery("SELECT p.titulo, i.nombreinvestigador FROM INVESTIGADOR i, PROYECTO p, ADJUDICA a "
                    + "WHERE a.rut = '"+rut + "' AND i.RUT='" + rut + "' AND a.codigoproyecto = "+codigoproyecto+" AND "
                            + " a.codigoproyecto = p.codigoproyecto");
            while(resultTitulo.next())
            {
                investigadorText.setText(resultTitulo.getString("nombreinvestigador") + ": " + rut);
                proyectoText.setText(resultTitulo.getString("titulo") + ": " + codigoproyecto);
            }
            
           
            
            
            
       
        }
        catch(Exception e){
                    JOptionPane.showMessageDialog(this, e.getMessage());    
        }
        
    }//GEN-LAST:event_formWindowOpened

