    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        try{
            MenuPrincipal.Conexion();  
            Statement s = MenuPrincipal.connection.createStatement();
            
            // AGREGA OPCIONES AL COMBOBOX DE INVESTIGADOR
            investigadorBox.removeAllItems();
            ResultSet resultInvestigador = s.executeQuery("SELECT nombreInvestigador, RUT FROM INVESTIGADOR");
            while(resultInvestigador.next())
            {
                investigadorBox.addItem(new Item(resultInvestigador.getString("RUT"), resultInvestigador.getString("nombreInvestigador")+": "+resultInvestigador.getString("RUT")));
            }   
            
           

        }
        catch(Exception e){
                    JOptionPane.showMessageDialog(this, e.getMessage());    
        }
        
        
    
    }//GEN-LAST:event_formWindowActivated

    private void editarInvestigadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarInvestigadorActionPerformed
        try{
            // OBTIENE EL RUT DEL POSTULANTE
            String rut = ((Item)investigadorBox.getSelectedItem()).getIdString();
            
            //cierra esta ventana
            this.setVisible(false);
            
            // va a la ventana de editar
            FormEditarInvestigador ventanita = FormEditarInvestigador.getInstanciaNueva(rut);
            ventanita.setVisible(true);
               
        }
        catch(Exception e){
            
                 JOptionPane.showMessageDialog(this, e.getMessage());   
        }
    }//GEN-LAST:event_editarInvestigadorActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            MenuPrincipal.Conexion();
            Statement s = MenuPrincipal.connection.createStatement();
            
            // ELIMINA INVESTIGADOR SELECCIONADO
            Item invSeleccionado = (Item)(investigadorBox.getSelectedItem());
            int z = s.executeUpdate("DELETE FROM INVESTIGADOR WHERE RUT='"
            +invSeleccionado.getIdString()+"'");
            
            if(z==1)
            {
                JOptionPane.showMessageDialog(this, "Investigador eliminado exitosamente");
            }
               
        }
        catch(SQLException e){
                 
                 if(e.getSQLState().equals("23503"))
                 {
                     JOptionPane.showMessageDialog(this, "No se puede eliminar al investigador porque est√° involucrado en otras relaciones");
                 }
                     
                 
                    
        }
    }//GEN-LAST:event_jButton1ActionPerformed

