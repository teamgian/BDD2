    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try{
            MenuPrincipal.Conexion();  
            Statement s = MenuPrincipal.connection.createStatement();
            
            
            // investigador revista
            String resultadoConsulta1 = "";
            ResultSet resultConsulta = s.executeQuery("select nombre_uni, cantidad_inv_senior"
                    + " from UNI_INVESTIGADOR");
            while(resultConsulta.next())
            {
                resultadoConsulta1 = resultadoConsulta1  + resultConsulta.getString("nombre_uni")
                +"\t\t             "+resultConsulta.getString("cantidad_inv_senior")+ "\n";
            }               
            resultadoProyectos_postulados.setText(resultadoConsulta1);
            
           
            
            String resultadoConsulta11 = "";
            ResultSet resultConsulta2 = s.executeQuery("select u.nombre, c.nombreciudad, r.nombreregion, v.cantidad_inv_senior\n" +
"from UNIVERSIDAD u, CIUDAD c, REGION r, UNI_INVESTIGADOR v \n" +
"where u.codigociudad=c.codigociudad AND c.codigoregion=r.codigoregion\n" +
"	AND u.nombre=v.nombre_uni AND v.cantidad_inv_senior=(select max(z.cantidad_inv_senior)\n" +
"													from UNI_INVESTIGADOR z)");
            while(resultConsulta2.next())
            {
                resultadoConsulta11 = resultadoConsulta11  + resultConsulta2.getString("nombre")
                +"\t";
                if(resultConsulta2.getString("nombreciudad").length()>14){
                    resultadoConsulta11 = resultadoConsulta11  + resultConsulta2.getString("nombreciudad")+"\t\t"+ resultConsulta2.getString("nombreregion");
                }
                else
                {
                     resultadoConsulta11 = resultadoConsulta11  + resultConsulta2.getString("nombreciudad")+"\t\t\t"+ resultConsulta2.getString("nombreregion");
                }
                
                
                resultadoConsulta11 = resultadoConsulta11  +"\t\t"+ resultConsulta2.getString("cantidad_inv_senior")+"\n";
                
                
                        
            }   
            
            resultadoConsulta2.setText(resultadoConsulta11);
            
        }
        catch(Exception e){
                    JOptionPane.showMessageDialog(this, e.getMessage());    
        }
        
        
    
    }//GEN-LAST:event_formWindowOpened

