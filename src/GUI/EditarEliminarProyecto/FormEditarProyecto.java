    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try{
            MenuPrincipal.Conexion();  
            Statement s = MenuPrincipal.connection.createStatement();
            
            ResultSet resultTitulo = s.executeQuery("SELECT titulo FROM PROYECTO WHERE codigoproyecto="
                    + proyecto.getIdString());
            while(resultTitulo.next())
            {
                tituloNuevoText.setText(resultTitulo.getString("titulo"));
            }
            
            // AGREGA OPCIONES AL COMBOBOX DE AREA
            areaBox.removeAllItems();
            ResultSet resultArea = s.executeQuery("SELECT codigoarea, descripcion FROM AREA");
            while(resultArea.next())
            {
                areaBox.addItem(new Item(resultArea.getString("codigoarea"), resultArea.getString("descripcion")));
            }   
            
            tipoBox.removeAllItems();
            // AGREGA OPCIONES AL COMBOBOX DE tipoproeycto
            ResultSet resultTipo = s.executeQuery("SELECT codigotipo, descripciontipo FROM tipoProyecto");
            while(resultTipo.next())
            {
                tipoBox.addItem(new Item(resultTipo.getString("codigotipo"), resultTipo.getString("descripciontipo")));
            }
            
        }
        catch(Exception e){
                    JOptionPane.showMessageDialog(this, e.getMessage());    
        }
        
    }//GEN-LAST:event_formWindowOpened

