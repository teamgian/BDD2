    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            
            MenuPrincipal.Conexion();
            Statement s = MenuPrincipal.connection.createStatement();
            
            
            // titulonuevo
            String tituloNuevo = tituloNuevoText.getText();  
            // TABLAS DE PROYECTO
            String fechadepostulacion = DateFormat.getDateInstance().format(fechaPostulacionChooser.getDate());
            String monto = montoText.getText();
            String duracion = duracionText.getText();
            String codigoarea = ((Item)areaBox.getSelectedItem()).getIdString();
            String codigotipo = ((Item)tipoBox.getSelectedItem()).getIdString();
            
            
            //edita el artículo con el nuevo dato
            int z = s.executeUpdate("UPDATE PROYECTO SET titulo='"
            +tituloNuevo+"', monto="+ monto + ", fechadepostulacion= '"+ fechadepostulacion+
                    "', duracion="+ duracion+", codigoarea="+codigoarea+", codigoTipo="+codigotipo
                    +" where codigoproyecto="+proyecto.getIdString());
            
            
            if(z == 1){ JOptionPane.showMessageDialog(this, "Se editó el proyecto exitosamente");  }
            
            
        } catch(Exception e)
        {
            
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

