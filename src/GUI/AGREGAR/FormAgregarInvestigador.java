    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String rut = rutText.getText();
        String nombreinv = nombreinvText.getText();                      
                       
        String calle = calleText.getText();            
        String num = numText.getText(); 
        try{
            
            MenuPrincipal.Conexion();
       
            // TABLAS DE INVESTIGADOR
            
             
            
            // ciudad en la que vive el investigador
            String codigociudad = ((Item)ciudadBox.getSelectedItem()).getIdString();
            
            // categoría del investigador
            String codigocategoria = ((Item)categoriaBox.getSelectedItem()).getIdString();
            
            Statement s = MenuPrincipal.connection.createStatement();
            
            int z=0;
            if(rut.length()==0 || nombreinv.length()==0 || calle.length()==0 || num.length()==0)
            {
                JOptionPane.showMessageDialog(this, "Llena campos vacíos");
            }
            else
            {
                //crea investigador con los datos proporcionados
            z = s.executeUpdate("INSERT INTO INVESTIGADOR(RUT, nombreInvestigador, codigociudad, codigocategoria, calle, num) values ('"
            +rut+"', '"+nombreinv+"', "+codigociudad+", "+codigocategoria+", '"+calle+"', "+num+")");
            }
            
                        
            // ASOCIA (O NO) AL INVESTIGADOR CON EL CENTRO DE INVESTIGACIÓN
            Item CISeleccionado = (Item)centroDeInvestigacionBox.getSelectedItem();
            Item rolSeleccionado = (Item)rolBox.getSelectedItem();
            
            if(!CISeleccionado.getName().equals("No asociar ningún centro"))
            {
                int x = s.executeUpdate("INSERT INTO TRABAJACI(rut, codigoCI, codigorol) values ("
                +rut+", "+CISeleccionado.getId() + ", " + rolSeleccionado.getId() + ")");             
            }
            
            // ASOCIA (O NO) AL INVESTIGADOR CON ALGUNA UNIVERSIDAD
            Item universidadSeleccionada = (Item)universidadBox.getSelectedItem();
            if(!universidadSeleccionada.getName().equals("No asociar a universidad"))
            {
                
                int y = s.executeUpdate("INSERT INTO PERTENECEUNIVERSIDAD(rut, codigouniversidad) values ("
                +rut + ", "+universidadSeleccionada.getId()+")");
            }
            
         
            if(z == 1){ JOptionPane.showMessageDialog(this, "Se agregó el investigador (y asociaciones) exitosamente");  }
            
            
        } catch(SQLException e){
                 
            System.out.println(e.getSQLState());
                 if(e.getSQLState().equals("23505"))
                 {
                     JOptionPane.showMessageDialog(this, "No se puede agregar la investigadora(o) debido a que ya existe una(o) con el rut: "
                     + rut);
                 }  
                 else if(e.getSQLState().equals("42703"))
                 {
                     JOptionPane.showMessageDialog(this, "Número telefónico inválido, por favor ingrese un número sin puntos ni otros caracteres");
                 }  
        }
    }//GEN-LAST:event_jButton1ActionPerformed

