    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        
        try{
            MenuPrincipal.Conexion();  
            Statement s = MenuPrincipal.connection.createStatement();
            
            // AGREGA OPCIONES AL COMBOBOX DE CIUDAD
            ResultSet resultCiudad = s.executeQuery("SELECT nombreciudad, codigociudad FROM CIUDAD");
            ciudadBox.removeAllItems();
            while(resultCiudad.next())
            {
                ciudadBox.addItem(new Item(resultCiudad.getString("codigociudad"), resultCiudad.getString("nombreciudad")));
            }   
            
            // AGREGA OPCIONES AL COMBOBOX DE CATEGORÍA
            ResultSet resultCategoría = s.executeQuery("SELECT codigocategoria, descripcioncategoría FROM categoria");
            categoriaBox.removeAllItems();
            while(resultCategoría.next())
            {
                categoriaBox.addItem(new Item(resultCategoría.getString("codigocategoria"), resultCategoría.getString("descripcioncategoría")));
            }
            
            // AGREGA OPCIONES AL COMBOBOX DE CENTRO DE INVESTIGACIÓN            
            ResultSet resultCI = s.executeQuery("SELECT nombre, codigoCI FROM CENTRODEINVESTIGACION"); 
            centroDeInvestigacionBox.removeAllItems();
            centroDeInvestigacionBox.addItem(new Item(0, "No asociar ningún centro"));
            while(resultCI.next())
            {
                centroDeInvestigacionBox.addItem(new Item(Integer.parseInt(resultCI.getString("codigoCI")), resultCI.getString("nombre")));
                
            }
            
            //AGREGA OPCIONES AL COMBOBOX DE ROL EN CENTRO DE INVESTIGACIÓN
            rolBox.removeAllItems();
            ResultSet resultRol = s.executeQuery("SELECT codigorol, descripcionrol FROM rol");
            while(resultRol.next())
            {
                rolBox.addItem(new Item(Integer.parseInt(resultRol.getString("codigorol")), resultRol.getString("descripcionrol")));
            }
            
            // AGREGA OPCIONES AL COMBOBOX DE UNIVERSIDAD
            ResultSet resultUniversidad = s.executeQuery("SELECT codigouniversidad, nombre FROM UNIVERSIDAD");
            universidadBox.removeAllItems();
            universidadBox.addItem(new Item(0, "No asociar a universidad"));
            while(resultUniversidad.next())
            {
                universidadBox.addItem(new Item(Integer.parseInt(resultUniversidad.getString("codigouniversidad")), resultUniversidad.getString("nombre")));
            }
            
            
            
        
        }
        catch(Exception e){
                    JOptionPane.showMessageDialog(this, e.getMessage());    
        }
        
        
    }//GEN-LAST:event_formWindowActivated

