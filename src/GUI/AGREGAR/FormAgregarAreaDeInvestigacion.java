    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            
            MenuPrincipal.Conexion();
       
            // TABLAS DE REGION
            String codigoArea = codigoText.getText();
            String descripcionArea = descripcionAreaText.getText();                      
                
            Statement s = MenuPrincipal.connection.createStatement();
            
            int z=0;
            if(codigoArea.length()==0 || descripcionArea.length()==0)
            {
                JOptionPane.showMessageDialog(this, "Llene campos en blanco");
            }else{
                //crea region
            z = s.executeUpdate("INSERT INTO AREA(codigoarea, descripcion) values("
            +codigoArea+", "+"'"+descripcionArea+"'"+")");
            }
            
            
            
          //si es agregado exitosamente va a la siguiente ventana
            if(z == 1){ 
                JOptionPane.showMessageDialog(this, "Área de investigación agregada exitosamente");
                        }
                        
        } catch(Exception e)
        {
            
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

