    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        
        try
        {
            MenuPrincipal.Conexion();  
            Statement s = MenuPrincipal.connection.createStatement();
            
            // AGREGA OPCIONES AL COMBOBOX DE REGION
            regionBox.removeAllItems();
            ResultSet resultInvestigador = s.executeQuery("SELECT nombreregion, codigoregion FROM REGION");
            while(resultInvestigador.next())
            {
                regionBox.addItem(new Item(resultInvestigador.getString("codigoregion"), resultInvestigador.getString("nombreregion")));
            } 
        
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e);
        }
        
    }//GEN-LAST:event_formWindowActivated

