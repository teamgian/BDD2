    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String codigoCiudad = códigoCiudadText.getText();
        try{
            
            MenuPrincipal.Conexion();
       
            // TABLAS DE CIUDAD
            
            String nombreCiudad = nombreCiudadText.getText();
            
            Item regionItem = (Item)regionBox.getSelectedItem();
            String codigoRegion = regionItem.getIdString();
                
            Statement s = MenuPrincipal.connection.createStatement();
            
            //crea investigador con los datos proporcionados
            int z=0;
            if(codigoCiudad.length()==0 || nombreCiudad.length()==0)
            {
                JOptionPane.showMessageDialog(this, "Llene campos en blanco");
            }else{
                z = s.executeUpdate("INSERT INTO CIUDAD(codigociudad, nombreciudad, codigoRegion) values("
            +codigoCiudad+", "+"'"+nombreCiudad+"'"+", "+codigoRegion+")");
            }
            
            
          //si es agregado exitosamente va a la siguiente ventana
            if(z == 1){ 
                JOptionPane.showMessageDialog(this, "Ciudad agregada exitosamente");
                        }
                        
        } catch(SQLException e){
                 
            
                 if(e.getSQLState().equals("23505"))
                 {
                     JOptionPane.showMessageDialog(this, "No se puede agregar ciudad ya que la clave: "+codigoCiudad+", ya está agregada al sistema");
                 }
                    
        }
    }//GEN-LAST:event_jButton1ActionPerformed

